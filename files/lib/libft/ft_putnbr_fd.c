#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long int num;

	num = nb;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		num = num * -1;
	}
	if (num >= 10)
	{
		ft_putnbr_fd(num / 10, fd);
		ft_putnbr_fd(num % 10, fd);
	}
	else
	{
		num = num + '0';
		ft_putchar_fd(num, fd);
	}
}
